p = 47
g = 5

print(f"Параметри системи: p = {p}, g = {g}")

#СЕКРЕТНІ КЛЮЧІ
a = 10  #Аліси
b = 15  #Боба

#ПУБЛІЧНІ КЛЮЧІ
# Формула: A = g^a mod p
A = pow(g, a, p)
# Формула: B = g^b mod p
B = pow(g, b, p)

print(f"Публічний ключ Аліси (A): {A}")
print(f"Публічний ключ Боба (B): {B}")

#ОБЧИСЛЕННЯ СПІЛЬНОГО СЕКРЕТУ
# Аліса бере ключ Боба і підносить до свого секрету a
K_alice = pow(B, a, p)

# Боб бере ключ Аліси і підносить до свого секрету b
K_bob = pow(A, b, p)

print("-" * 30)
print(f"Спільний секрет Аліси: K = {K_alice}")
print(f"Спільний секрет Боба:  K = {K_bob}")

if K_alice == K_bob:
    print(f"Сеансовий ключ для шифрування: {K_alice}")
